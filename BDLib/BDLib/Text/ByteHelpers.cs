        public static bool AreTheSame(byte[] a, byte[] b)
        {
            bool output = true;

            if (a.Length != b.Length)
                output = false;
            else
                for (int x = 0; x < a.Length; x++)
                {
                    if (a[x] != b[x])
                    {

                        output = false;
                        break;
                    }
                }

            return output;
        }
